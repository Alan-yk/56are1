(3)主麻清真寺，在伊朗较多。
(4)加米清真寺：为地区中心寺，亦称大寺。
(5)陵墓寺，附属于陵墓主体建筑的清真寺，如侯赛因清真寺。
(6)一般清真寺。</p>
