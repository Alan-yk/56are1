														<p>&nbsp;&nbsp;色调是建筑外观形象的一个重要特征。绿色象征大自然，象征生命，给予人们安宁、祥和的感觉，因此回族常用石绿、豆绿、蓝绿等比较稳重的调和色。白色也是回族穆斯林常用的颜色，多用在建筑的内外粉刷，形成了以白为主的风格。黄色是西北地区土生建筑的自然色，代表着回族穆斯林赖以生存的土地，有一种纯朴敦厚的感觉，带来欢乐与光明的联想。黄色用在建筑的各个部位，与其他色彩搭配结合，能打破单调感并产生很好的视觉效果。</p>
