														<p>&nbsp;&nbsp;“面花”，城里人叫“面塑”或“捏面人”。河南省豫东地区称为“蒸花馍”；豫西一带早年叫“窝窝花”，解放以后改称“糕花”，但在河南广大地区，叫的最多、最普遍的称呼是“捏面花”。 是用面捏制成的汉族民间艺术品，它普遍流行于中国以吃面食为主的北方，起源于汉族民间祭祀活动中用面塑动物代替宰杀牛羊等动物的习俗。<p></p>&nbsp;&nbsp;回族的面花艺术，以其制作工艺精湛，别具特色，味道鲜美而备受人们的赞誉，吸引了不少中外游客。
														现在回族面花艺术的品种达百余种。一品一形，百形百味。特别逢回族过节，或婚丧嫁娶、抓周、招待贵客等重要场合，只要你留意就会看到那琳琅满目，千姿百态的面花，使你一饱眼福。<p></p>
&nbsp;&nbsp;回族面花的造型大多源于自然界和社会生活中的各类花草、蝴蝶、鸽子等形态，同时，还大量地采用阿拉伯的卷草形、云纹形等几何图形。根据不同的需要和场合，通过擀、切、揉、捏、搓、挟、挤、压、画、点染等多种手法，制作出形式多样的造型，并用炸、蒸、烙、烤等烹饪技术、使形、色、香味于一体。如炸货类里有名的油香、馓子、饣果饣果、花花、油酥花茧、油酥脆花、油圈等，仅麻花一项可制作出三股麻花、绳子头麻花、大麻花、果料麻花、芙蓉麻花等。蒸货类里有千层糕、开花卷、金裹鱼、白牡丹、红花绿叶等。烤货类里有云纹岁糕、卷草形馄馍等。近几年，回族的一些面花还大胆采用象征、变形、夸张等艺术手法，使花纹巧妙，绚丽多彩，并赋予一定的意义。	
