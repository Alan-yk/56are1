													<div class="col-md-6">
														<p>&nbsp;&nbsp;清真寺是穆斯林群体的中心，唐、宋时期的“蕃坊”和明、清以来的“教坊”，都是以清真寺为中心，聚合周围村庄、街巷和居民点而形成的社区。每个清真寺在经济上、管理上自成一体，一般互不隶属。有的门宦在某一地区某个寺内派驻“热依斯”(即教主的教务代理人)行使管理，下辖若干寺坊，有的教派有“海乙寺”(即中心寺)，下辖“稍麻”小寺，形成较大的教坊。撒拉族地区历史上实行总寺，形成教坊统辖制。唐、宋、元、明、清时期，有的大寺实行“卡迪”掌教制，明、清以来实行伊玛目掌教制，民国以来实行阿訇掌教制，在管理形式上设“学董会”，当代则设“民主管理委员会”，负责管理寺务、财务，聘请阿訇执掌教务。以清真寺为中心的教坊，把分散的穆斯林凝聚为自然的整体，成为一个坊的宗教、教育、经济、文化中心，凡有关该坊穆斯林的宗教生活、经济大事或教育、文化、民事纠纷、婚丧礼仪、欢庆节日等等，都离不开清真寺。清真寺在穆斯林心目中具有崇高、神圣的地位，成为伊斯兰文化的象征。清真寺的经济主要来自教民的捐赠及亡人遗嘱捐献的地产、房产作为宗教基金，用以支付阿訇、经生的生活费、日常寺务的开支和节日的用度。</p>
														<p>&nbsp;&nbsp;早期清真寺的建筑简朴无华，如麦地那先知寺主要由围墙圈成院落供礼拜，房顶供唤拜，再设一简单讲台供宣教即可。其后随着穆斯林建筑艺术的发展，结构严整、雄伟壮丽和带有装饰艺术的建筑群相继出现。清真寺的主体建筑是礼拜大殿，方向朝向麦加克尔白。大殿正面墙中ib有凹壁(米哈拉布)，左前方有阶梯形讲坛(敏白尔)。较大的清真寺还有宣礼塔。塔顶呈尖形，故称尖塔，系唤拜之用。一般清真寺有l～4个尖塔，土耳其伊斯坦布尔的素丹艾哈迈德清真寺有6塔，麦加圣寺有7塔。还有沐浴用的水房。在伍麦叶王朝哈里发瓦利德时期(705～715)出现了穹窿建筑，多数是由分行排列的方柱或圆柱支撑的一系列拱门，拱门又支撑着圆顶、拱顶。建筑物外表，敷以彩色或其他装潢。其后，清真寺建筑艺术形成四大流派和风格：(1)叙利亚一埃及派：主要以希腊、罗马和当地样式为范例。(2)伊拉克一波斯派：以萨珊式样、古代迦勒底式样和亚述式样为基础。(3)西班牙一北非派：即马格里布式样的马蹄形。(4)印度派：大多为圆顶形，有明显的印度建筑色彩。清真寺的管理在伊斯兰国家多由政府宗教基金部门领导，人员少，事务简单，教长不一定住寺，清真寺一般都有宗教公产(瓦克夫)和基金。</p>
