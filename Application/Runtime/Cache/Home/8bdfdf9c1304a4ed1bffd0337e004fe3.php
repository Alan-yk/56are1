														<p>&nbsp;&nbsp;回族由于受伊斯兰文化艺术的影响，民间刺绣以其独特的风格，广泛流传，受到人们的青睐。

    <p></p>&nbsp;&nbsp;在回族聚居区，特别是山区，回族姑娘们常常以刺绣比聪明，争巧手，看本事，视刺绣为一种吉祥的艺术情趣。每个姑娘或媳妇，一般都有一个刺绣用的小包袱，平时注意购置各种刺绣针、剁针和花丝线。每当走乡串户的货郎担来到山乡村头时，妇女们争相选购五颜六色的花线。到农闲时，便互相串门，三个一伙、五个一群地坐在一起，拿出绣花绷，忙起刺绣活计。回族妇女的刺绣都有自己的巧妙构思和飞针走线的“套路”，姑娘和媳妇们坐在一起，取长补短，互相竞争，越绣越美，越绣花样越多，真可谓百花盛开，多姿多彩。鞋头、袜底、鞋垫、坎肩、经挂、裹肚、香包、缠腰、枕顶、枕套、帽沿、耳套、荷包、围裙、盖头、褥面等用品上刺绣着各种花纹和图案。

    <p></p>&nbsp;&nbsp;回族的刺绣富有想像力和艺术魁力。虽然伊斯兰教义禁止崇拜偶像，禁止在造型艺术中表现有生命的物体（人和动物），但她们创作日常生活用品和鸟类动物时，往往绣成一种错综复杂的图形，使鸟类等形体交织在花草图案中，以求形似。

    <p></p>&nbsp;&nbsp;刺绣花草图案和几何图形，是回族妇女刺绣的绝技。在西北山区回民家里，我们看到她们的枕头、坎肩、挂图、围裙等处绣的花卉，形象非常逼真，形似蝴蝶的花朵，娇嫩的花瓣，淡雅恬静，仿佛使人感到散发着暗香。有些刺绣作品，很注意变化装饰，给人以整体美。她们往往撷取大自然中各种不同的植物叶，构成自己想象中的花草树木，技与叶、花与蔓和谐地统一，有点像汉族人刺绣图案中的百花百果一棵树的创作方法。苏州回族妇女全静芬的刺绣作品，轰动了我国刺绣界，曾在国际上获奖。

    <p></p>&nbsp;&nbsp;回族妇女的刺绣，不仅表现自然美、生活美，而且还艺术地表现其宗教信仰。当回族姑娘或媳妇买回一块干净的布料后，洗完大小净便请阿匍书写上阿文中堂，然后便不分昼夜，千针万线地刺绣。绣完字，一般还用传统的阿拉伯几何图形或云纹样绣上花边，挂在墙上，颇受人们的喜爱与好评，充分体现了回族妇女的审美心理。</p>
